def d_s(lcd, f, s_x, s_y, p_s):
    for r in range(16):
        c = 0
        while c < 16:
            s_c = c
            v = f[r][c]
            while c < 16 and f[r][c] == v:
                c += 1
            w = c - s_c
            
            x_p = s_x + (s_c * p_s)
            y_p = s_y + (r * p_s)
            
            lcd.fillRect(x_p, y_p, w * p_s, p_s, C[v])